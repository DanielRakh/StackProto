#import "UIColor+ColorPalette.h"

@implementation UIColor (ColorPalette)

+ (UIColor *)offWhiteColor {
    return [UIColor colorWithRed:0.996 green:0.996 blue:0.996 alpha:1.0];
}

+ (UIColor *)backgroundWhiteColor {
    return [UIColor colorWithRed:0.988 green:0.988 blue:0.988 alpha:1.0];
}

+ (UIColor *)midnightBlueColor {
    return [UIColor colorWithRed:0.204 green:0.259 blue:0.353 alpha:1.0];
}

+ (UIColor *)lightBlueColor {
    return [UIColor colorWithRed:0.373 green:0.671 blue:0.914 alpha:1.0];
}

+ (UIColor *)lightPurpleColor {
    return [UIColor colorWithRed:0.329 green:0.443 blue:0.804 alpha:1.0];
}

+ (UIColor *)darkPurpleColor {
    return [UIColor colorWithRed:0.286 green:0.310 blue:0.678 alpha:1.0];
}

@end
